package ConditionalsAndLoops;

public class TheOrOperator {
    public static void main(String[] args) {
        int a = 11; int b = 12;
        int c = 40;
        if (a > 100 || b > 3) {
            System.out.println(a);
        } else {
            System.out.println(c);
        }
    }
}
